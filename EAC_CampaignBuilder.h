    static constexpr unsigned int SPACE_BETWEEN_PARAGRAPHS = 20;
    static constexpr unsigned int SPACE_ABOVE_BELOW_BUTTON = 35;
    static constexpr unsigned int SPACE_BETWEEN_PRE_LAST = 60;
    static constexpr unsigned int SPACE_ABOVE_SENDER = 10;
    static constexpr unsigned int SPACE_ZERO = 0;
    static constexpr unsigned int SPACE_BEFORE_LIST = 10;

    void InsertSpacingNodes(std::vector<std::unique_ptr<ICampaignNode>>& processedNodes)
    {
        if (m_campaignNodes.empty())
        {
            return;
        }

        std::vector<NodeType> nodeTypes{};
        for (const auto& campaignNode : m_campaignNodes)
        {
            nodeTypes.push_back(campaignNode->GetNodeType());
        }

        for (size_t k = 0; k < m_campaignNodes.size(); ++k)
        {
            processedNodes.push_back(std::move(m_campaignNodes[k]));
            if (k == m_campaignNodes.size() - 1)
            {
                break;
            }

            NodeType currentNodeType = nodeTypes[k];
            NodeType nextNodeType = nodeTypes[k + 1];

            unsigned int spacingNodeHeight = GetSpacingHeightBySemantic(currentNodeType, nextNodeType, k, nodeTypes);
            if (spacingNodeHeight > 0)
            {
                processedNodes.push_back(std::make_unique<SpacingNode>(spacingNodeHeight));
            }
        }
    }

    unsigned int GetSpacingHeightBySemantic(NodeType curr, NodeType next, size_t idx, const std::vector<NodeType>& nodeTypes)
    {
        bool isNextLastReal = true;
        for (size_t k = idx + 1; k < nodeTypes.size(); ++k)
        {
            if (nodeTypes[k] != NodeType::SENDER_NODE && nodeTypes[k] != NodeType::SPACING_NODE)
            {
                isNextLastReal = false;
                break;
            }
        }

        if (next == NodeType::BUTTON_NODE || next == NodeType::BUTTONS_ROW_NODE)
        {
            if (isNextLastReal)
                return SPACE_BETWEEN_PRE_LAST;
            else
                return SPACE_ABOVE_BELOW_BUTTON;
        }

        if ((curr == NodeType::BUTTON_NODE || curr == NodeType::BUTTONS_ROW_NODE) && next == NodeType::PARAGRAPH_NODE)
        {
            if (isNextLastReal)
                return SPACE_BETWEEN_PRE_LAST;
            else
                return SPACE_ABOVE_BELOW_BUTTON;
        }

        if (next == NodeType::PARAGRAPH_NODE && isNextLastReal)
        {
            return SPACE_BETWEEN_PRE_LAST;
        }

        if (curr == NodeType::PARAGRAPH_NODE && next == NodeType::PARAGRAPH_NODE)
        {
            return SPACE_BETWEEN_PARAGRAPHS;
        }

        if (next == NodeType::SENDER_NODE)
        {
            return SPACE_ABOVE_SENDER;
        }

        if (curr == NodeType::SENDER_NODE)
        {
            return SPACE_ZERO;
        }

        if (next == NodeType::LABELED_LIST_NODE || next == NodeType::DOT_LIST_NODE)
        {
            return SPACE_BEFORE_LIST;
        }

        return SPACE_BETWEEN_PARAGRAPHS;
    }

    void AdjustLastParagraphSpacing(std::vector<std::unique_ptr<ICampaignNode>>& nodes)
    {
        if (nodes.empty()) 
        {
            return;
        }

        int lastParagraphIdx = -1;
        for (int i = static_cast<int>(nodes.size()) - 1; i >= 0; --i)
        {
            if (nodes[i]->GetNodeType() == NodeType::PARAGRAPH_NODE)
            {
                lastParagraphIdx = i;
                break;
            }
        }

        if (lastParagraphIdx == -1) 
        {
            return;
        }

        if (lastParagraphIdx > 0)
        {
            auto& prevNode = nodes[lastParagraphIdx - 1];
            if (prevNode->GetNodeType() == NodeType::SPACING_NODE)
            {
                auto* spacing = dynamic_cast<SpacingNode*>(prevNode.get());
                if (spacing) spacing->SetSpacingHeight(SPACE_BETWEEN_PRE_LAST);
                return;
            }
        }

        auto spacing = std::make_unique<SpacingNode>(SPACE_BETWEEN_PRE_LAST);
        nodes.insert(nodes.begin() + lastParagraphIdx, std::move(spacing));
    }


