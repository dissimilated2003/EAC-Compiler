// к внешнему виду рассылки заказчиками
struct ICampaignBuilder
{
    virtual std::string BuildCampaignHTML() = 0;
    virtual void AddNode(std::unique_ptr<ICampaignNode>&& node) = 0;

    virtual ~ICampaignBuilder() = default;
};


